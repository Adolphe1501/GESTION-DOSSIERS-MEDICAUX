                <p>Le coronavirus se défini comme une famille de virus variés. Les coronavirus peuvent infecter aussi bien l’homme que l’animal. Leur nom signifie "virus en couronne" et vient du fait qu'ils possèdent tous un aspect en forme de couronne vu du microscope. Les coronavirus ont été identifiés pour la première foischez l'humain dans les années 1960.Il s'agit de virus causant des maladies émergentes, c'est-à-dire des infections nouvelles dues à des modifications ou à des mutations du virus. Cependant deux d’entre eux ont entrainé de graves épidémies : le SRAS-CoV en 2002 et le Mers-CoV en 2012 et aujourd’hui, un nouveau coronavirus (SARS-CoV-2) a été identifié début janvier 2020 comme étant à l’origine de cas groupés de pneumonies en Chine. Le 30 janvier 2020, cette maladie a été déclarée Urgence de Santé Publique de Portée Internationale (USPPI) par le Directeur Général de l’OMS au regard de la propagation rapide de cette épidémie.</p>
